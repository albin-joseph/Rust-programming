fn main() {
    //Int
    let x: i32 = -42;
    let y: u32 = 10;
    println!("Signed Integer: {}", x);
    println!("Unsigned Integer: {}", y);

    //Float
    let pi: f64 = 3.14;
    println!("pi value: {}", pi);

    //Boolean
    let is_raining: bool = true;
    println!("is raining?: {}", is_raining);

    //char
    let letter: char = 'a';
    println!("first letter of the alphabets: {}", letter);
}
